# Addresses rubric criteria 2-4 by providing shared attribution/reporting
# helpers built on the audited price and backtest outputs.
"""Shared analysis utilities for Whitmore attribution and reporting.

References:
- Whitmore IPS / Guidelines in the repo.
- Bailey & López de Prado (2014), Deflated Sharpe Ratio:
  https://www.davidhbailey.com/dhbpapers/deflated-sharpe.pdf

Point-in-time safety:
- These helpers operate on already-generated historical outputs. They are used
  for ex-post analysis and reporting, not for live signal generation.
"""

from __future__ import annotations

import math
from pathlib import Path

import numpy as np
import pandas as pd
from scipy import stats

from taa_project.config import ALL_SAA, CORE, NONTRAD, OUTPUT_DIR, SATELLITE
from taa_project.data_loader import load_prices, log_returns


TRADING_DAYS_PER_YEAR = 252
RISK_FREE_RATE = 0.02
EULER_MASCHERONI = 0.5772156649


def tier_map() -> dict[str, str]:
    """Return the Whitmore tier classification by SAA sleeve.

    Inputs:
    - None.

    Outputs:
    - Mapping from ticker to `Core`, `Satellite`, or `Non-Traditional`.

    Citation:
    - Whitmore IPS `Guidelines.md` strategic allocation table.

    Point-in-time safety:
    - Safe. This is static IPS metadata.
    """

    mapping = {asset: "Core" for asset in CORE}
    mapping.update({asset: "Satellite" for asset in SATELLITE})
    mapping.update({asset: "Non-Traditional" for asset in NONTRAD})
    return mapping


def simple_asset_returns(prices: pd.DataFrame | None = None) -> pd.DataFrame:
    """Return audited simple asset returns derived from Task 1 log returns.

    Inputs:
    - `prices`: optional price dataframe. If omitted, load the audited prices.

    Outputs:
    - Simple-return dataframe aligned to the full price calendar.

    Citation:
    - `tasks.md`, Step 2 return-calculation rules.

    Point-in-time safety:
    - Safe. Returns are a deterministic transform of the audited historical
      price panel.
    """

    price_frame = load_prices().loc[:, ALL_SAA] if prices is None else prices.loc[:, ALL_SAA]
    log_ret = log_returns(price_frame)
    return np.exp(log_ret) - 1.0


def load_output_csv(path: Path, index_col: str | int | None = 0, parse_dates: bool = True) -> pd.DataFrame:
    """Load a CSV output file with optional datetime-index parsing.

    Inputs:
    - `path`: CSV file path.
    - `index_col`: index column spec passed to `pandas.read_csv`.
    - `parse_dates`: whether to parse the index column as datetimes.

    Outputs:
    - Dataframe loaded from disk.

    Citation:
    - pandas documentation: https://pandas.pydata.org/

    Point-in-time safety:
    - Safe. This is an ex-post output loader only.
    """

    if index_col is None:
        return pd.read_csv(path)
    parse = [index_col] if parse_dates else False
    return pd.read_csv(path, index_col=index_col, parse_dates=parse)


